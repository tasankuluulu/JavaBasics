package com.syntax.class0;

public class VariableValues {
	public static void main (String[] args) {
		
		byte a = 10;
		byte b;
		b = 100;
		byte c, d;
		c = 30;
		d = 40;
		
		System.out.println("Byte variables:");
		System.out.println(a);
		System.out.println(b);
		System.out.println(c);
		System.out.println(d);
		
		short num1 = 10;
		short num2;
		num2 = 20;
		short num3, num4;
		num3 = 30;
		num4 = 30;
		
		System.out.println("Short variables:");
		System.out.println(num1);
		System.out.println(num2);
		System.out.println(num3);
		System.out.println(num4);
		
		int a1 = 10;
		int a2;
		a2 = 20;
		int a3, a4;
		a3 = 30;
		a4 = 40;
		
		System.out.println("Int variables:");
		System.out.println(a1);
		System.out.println(a2);
		System.out.println(a3);
		System.out.println(a4);
		
		long b1 = 102019l;
		long b2;
		b2 = 12038372l;
		long b3, b4;
		b3 = 4545545l;
		b4 = 459555556l;
		
		System.out.println("Long variables:");
		System.out.println(b1);
		System.out.println(b2);
		System.out.println(b3);
		System.out.println(b4);
		
		float c1 = 154.542f;
		float c2;
		c2 = 45.25f;
		float c3, c4;
		c3 = -595.25f;
		c4 = -659.2f;
		
		System.out.println("Float variables:");
		System.out.println(c1);
		System.out.println(c2);
		System.out.println(c3);
		System.out.println(c4);
		
		double d1 = -145.25;
		double d2;
		d2 = 451.266;
		double d3, d4;
		d3 = 1212.2;
		d4 = -12.01;
		
		System.out.println("Double variables:");
		System.out.println(d1);
		System.out.println(d2);
		System.out.println(d3);
		System.out.println(d4);
		
		char e1 = '4';
		char e2;
		e2 = '+';
		char e3, e4;
		e3 = 'w';
		e4 = '@';
		
		System.out.println("Char variables:");
		System.out.println(e1);
		System.out.println(e2);
		System.out.println(e3);
		System.out.println(e4);
		
		boolean f1 = true;
		boolean f2;
		f2 = false;
		boolean f3, f4;
		f3 = true;
		f4 = false;
		
		System.out.println("Boolean variables:");
		System.out.println(f1);
		System.out.println(f2);
		System.out.println(f3);
		System.out.println(f4);
		
	}
}
