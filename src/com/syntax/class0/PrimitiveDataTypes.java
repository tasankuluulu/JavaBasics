package com.syntax.class0;

public class PrimitiveDataTypes {
	public static void main(String[] args) {
		/* Primitive Data Types
		 * byte
		 * short
		 * int
		 * boolean
		 * double
		 * char
		 * 
		 * 
		 * 
		 * 
		 * 
		 */
		// whole number data types
		byte num = 10;
		short num1 = 100;
		int num2 = 1000;
		long num3 = 10000000;
		long num4 = 1033985984523843333l;
		long num5 = 151251122L;
		
		// decimal number data types
		float num6 = 12.9f;
		float num7 = 12.9F;
		// long and float only variables you need to add letters L and F
		double num8 = 0.888;
		
		// non-number data types
		char add = '+';
		char letter = 'a';
		
		// True or False (yes or no)
		boolean hungry = true;
		boolean thirsty = false;
		
		
		
		}

}
